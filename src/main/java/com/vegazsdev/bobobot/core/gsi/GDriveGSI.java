package com.vegazsdev.bobobot.core.gsi;

@SuppressWarnings("unused")
public class GDriveGSI {

    private String ab;
    private String a;
    private String folder;

    GDriveGSI() {
    }

    String getAb() {
        return ab;
    }

    void setAb(String ab) {
        this.ab = ab;
    }

    String getA() {
        return a;
    }

    void setA(String a) {
        this.a = a;
    }

    String getFolder() {
        return folder;
    }

    void setFolder(String folder) {
        this.folder = folder;
    }
}